package com.java.samples;

public class star {
	static int space=2;
	static int star=1;
	public static void main (String [] args)
	{
		for (int i = 0; i < 4; i++) {
			for (int j = 4; j < i; j--) {
				System.out.print("*");
				System.out.println();
			star=star;
			space=space+1;
			}
			
		}
	}

}
